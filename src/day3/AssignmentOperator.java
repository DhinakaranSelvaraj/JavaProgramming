package day3;

public class AssignmentOperator {

	public static void main(String[] args) {
		//Assignment Operator [== += -= *= /= %=]
		
		//Case 1 
		int a = 10;
		a = a+5;
		System.out.println(a);
		
		//Case 2 [+=]
		int b = 10;
		b+=5;
		System.out.println(b);
		
		//Case 3 [-=]
		int c = 10;
		c-=5;
		System.out.println(c);
		
		//Case 4 [*=]
		int d = 10;
		d*=5;
		System.out.println(d);
		
		//Case 5 [/=]
		int e = 10;
		e/=2;
		System.out.println(e);
		
		//Case 6 [/=]
		int  f =10;
		f%=2;
		System.out.println(f);
	}

}
