package day2;

public class DataTypes {

	public static void main(String[] args) {

		// Numeric Data type
		int a = 100, b = 200;
		System.out.println("The value os A is:" + a);
		System.out.println("The value os B is:" + b);
		System.out.println("The sum of A and B is: " + (a + b));

		byte by = 125;
		System.out.println(by);

		short sh = 3232;
		System.out.println(sh);

		long l = 12321321255L; // use lower case or upper case L in last
		System.out.println(l);
		// for long and float we use literal (l and f)

		// Decimal numbers - float, double
		float item_prize = 15.56F; // Literal needed
		System.out.println(item_prize);

		double dbl = 1225.33648448;
		System.out.println(dbl);

		// Character
		char grade = 'A';
		System.out.println(grade);

		// Boolean
		boolean bl = true;
		System.out.println(bl);
		boolean b2 = false;
		System.out.println(b2);
		

	}

}
